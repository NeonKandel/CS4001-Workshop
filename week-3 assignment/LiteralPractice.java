public class LiteralPractice {
    public static void main(String[] args) {
        long bigNumber = 9876543210L;
        float price = 19.99f;
        char copyrightSymbol = '\u00A9'; // \u00A9= sym for copyright....*
        
        System.out.println("--- Literal Practice Results ---");
        System.out.println("Long value (with L):\t" + bigNumber);
        System.out.println("Float value (with f):\t" + price);
        System.out.println("Unicode Char symbol:\t" + copyrightSymbol);
        
        
        
    }
}