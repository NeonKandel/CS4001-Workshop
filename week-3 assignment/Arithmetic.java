public class Arithmetic {
    public static void main(String[] args) {
        int a = 2, b = 4;
        System.out.println("add: " + (a + b));     
        System.out.println("sub: " + (a - b));
        System.out.println("mult: " + (a * b));     
        System.out.println("div: " + (a / b));      
        System.out.println("modulus: " + (a % b));  
    }
}