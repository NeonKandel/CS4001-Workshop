public class DataTypeInspector {
    public static void main(String[] args) {
        byte myByte = 100;
        short myShort = 5000;
        int myInt = 150000;
        long myLong = 9000000000L;
        float myFloat = 5.75f;
        double myDouble = 19.99;
        char myChar = 'A';
        boolean myBool = true;
        System.out.println("--- Java Primitive Data Types ---");
        System.out.println("Byte value:\t" + myByte);
        System.out.println("Short value:\t" + myShort);
        System.out.println("Int value:\t" + myInt);
        System.out.println("Long value:\t" + myLong);
        System.out.println("Float value:\t" + myFloat);
        System.out.println("Double value:\t" + myDouble);
        System.out.println("Char value:\t" + myChar);
        System.out.println("Boolean value:\t" + myBool);
    }
}